public class SagesMessageHeader {

	private String header;

	/**
	 * SagesMessageHeader constructor
	 * @param header
	 */
	public SagesMessageHeader(String header) {
		super();
		this.header = header;
	}

	/**
	 * @return the header
	 */
	public String getHeader() {
		return header;
	}

	/**
	 * @param header the header to set
	 */
	public void setHeader(String header) {
		this.header = header;
	}
	
	
}
